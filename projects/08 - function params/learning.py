def greet(name):
    print(f"Hello {name}")
    print(f"How do you do {name}?")

greet("Chris")
# The var is the parameter (name in this case)
# the argument is the value of the variable

def greet_with(name, location):
    print(f"Hello {name}")
    print(f"What is it like in {location}")

greet_with(location="Tacoma", name="Chris")
